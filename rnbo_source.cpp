void gen_01_perform(
    number in1,
    number in2,
    number N,
    SampleValue * out1,
    SampleValue * out2,
    Index n
) {
    RNBO_UNUSED(N);
    RNBO_UNUSED(in2);
    RNBO_UNUSED(in1);
    auto __gen_01_len_value = this->gen_01_len_value;
    auto tables_dim_1_0 = this->dim(this->gen_01_tables);
    auto tables_chans_2_1 = this->channels(this->gen_01_tables);
    number div_3_2 = tables_dim_1_0 / (number)64;
    auto wrap_4_3 = this->wrap(0, 0, 1);
    number sub_5_4 = 0 - wrap_4_3;
    auto wrap_6_5 = this->wrap(sub_5_4, 0, 64);
    number add_11_9 = sub_5_4 + 1;
    auto wrap_12_10 = this->wrap(add_11_9, 0, 64);
    number len_next_18_15 = fixdenorm(div_3_2);
    Index i;

    for (i = 0; i < n; i++) {
        out2[(Index)i] = __gen_01_len_value;
        number mul_7_6 = wrap_6_5 * __gen_01_len_value;
        number add_8_7 = mul_7_6 + __gen_01_len_value;
        number sample_tables_9 = 0;
        number index_tables_10 = 0;
        auto result_8 = this->wave_default(this->gen_01_tables, 0, mul_7_6, add_8_7, 0);
        index_tables_10 = result_8[1];
        sample_tables_9 = result_8[0];
        number mul_13_11 = wrap_12_10 * __gen_01_len_value;
        number add_14_12 = mul_13_11 + __gen_01_len_value;
        number sample_tables_15 = 0;
        number index_tables_16 = 0;
        auto result_13 = this->wave_default(this->gen_01_tables, 0, mul_13_11, add_14_12, 0);
        index_tables_16 = result_13[1];
        sample_tables_15 = result_13[0];
        number mix_17_14 = sample_tables_9 + wrap_4_3 * (sample_tables_15 - sample_tables_9);
        out1[(Index)i] = mix_17_14;
        __gen_01_len_value = len_next_18_15;
    }

    this->gen_01_len_value = __gen_01_len_value;
}

